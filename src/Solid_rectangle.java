public class Solid_rectangle {

	public static void main(String[] args) {
		// solid_rectangle
		
		int n = 4;
		int m = 5;
		
		//outer Loop
		for(int i=1;i<=n;i++) {
		   //inner loop
			for(int j=1;j<=m;j++) {
				System.out.print("*");
			}//print next line
			System.out.println("");
		}
		
	}

}
